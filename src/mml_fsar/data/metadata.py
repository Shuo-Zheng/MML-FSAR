def class_names_for_dataset(
    name: str,
) -> tuple[tuple[str, ...], tuple[str, ...], tuple[str, ...]]:
    """Return default train/valid/test class names for a migrated dataset."""

    return DATASET_CLASS_NAMES.get(str(name).lower(), ((), (), ()))


def configured_class_names(
    dataset: dict[str, Any],
) -> tuple[tuple[str, ...], tuple[str, ...], tuple[str, ...]]:
    """Resolve class names from YAML first, then built-in migrated metadata."""

    default_train, default_valid, default_test = class_names_for_dataset(
        str(dataset.get("name", ""))
    )
    train_class_names = tuple(dataset.get("train_class_names") or default_train)
    valid_class_names = tuple(dataset.get("valid_class_names") or default_valid)
    test_class_names = tuple(dataset.get("test_class_names") or default_test)
    return train_class_names, valid_class_names, test_class_names
